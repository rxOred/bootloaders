;---------------------------------------------------------------------------------------
;initialize screen
;---------------------------------------------------------------------------------------

InitScreen:
  pusha

  mov     ah, 0x07
  mov     al, 0x00
  mov     bh, 0x07
  mov     cx, 0x0000
  mov     dh, 0x18
  mov     dl, 0x4f
  int     0x10

  popa
  ret

;---------------------------------------------------------------------------------------
;move cursor dh, dl = position
;---------------------------------------------------------------------------------------

MoveMouse:
  pusha

  mov     ah, 0x02
  mov     bh, 0x00
  int     0x10

  popa
  ret

;-----------------------------------------------------------------------------------------
;Print strings to screen  si = string to print
;-----------------------------------------------------------------------------------------

Print:
  pusha
  mov     ah, 0x0E
  mov     bl, 0x00

.loop:
  mov     al, [si]
  cmp     al, 0
  jz      .finish
  int     0x10
  inc     si
  jmp     .loop

.finish:
  popa
  ret
